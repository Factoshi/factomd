#!/usr/bin/env bash

function stashlogs() {
  tar -czf $1.tgz simTest/*.txt
}

GIVEN_NODES=LF MAX_BLOCKS=40 BATCHES=10 ENTRIES=1000 DELAY_BLOCKS=1 DROP_RATE=100 ./test.sh
stashlogs test_LF_d100

GIVEN_NODES=LLF MAX_BLOCKS=40 BATCHES=10 ENTRIES=1000 DELAY_BLOCKS=1 DROP_RATE=100 ./test.sh
stashlogs test_LLF_d100

GIVEN_NODES=LLLF MAX_BLOCKS=40 BATCHES=10 ENTRIES=1000 DELAY_BLOCKS=1 DROP_RATE=100 ./test.sh
stashlogs test_LLLF_d100

GIVEN_NODES=LLLLF MAX_BLOCKS=40 BATCHES=10 ENTRIES=1000 DELAY_BLOCKS=1 DROP_RATE=100 ./test.sh
stashlogs test_LLLLF_d100
